import 'dart:convert';
import 'ext.dart';

void day4() {
  final input = LineSplitter()
      .convert(_input)
      .map((line) => line.split(""))
      .toList();

  int result1 = 0;
  int helper(List<List<String>> diagram) {
    int result = 0;
    List<(int, int)> changes = [];
    for (int i = 0; i < diagram.length; i++) {
      for (int j = 0; j < diagram.first.length; j++) {
        final c = diagram[i][j];
        if (c != '@') continue;
        if (_getAdjucentRolls(diagram, i, j) < 4) {
          result++;
          changes.add((i, j));
        }
      }
    }
    for (final pair in changes) {
      diagram[pair.$1][pair.$2] = '.';
    }
    return result;
  }

  int result2 = result1;
  int additions = 0;
  do {
    additions = helper(input);
    result2 += additions;
    if (result1 == 0) result1 = additions;
  } while (additions > 0);

  print("day 4 result1: $result1 result2: $result2");
}

int _getAdjucentRolls(List<List<String>> diagram, int i, int j) {
  int result = 0;
  final top = diagram.getOrNull(i - 1);
  final current = diagram.elementAt(i);
  final bot = diagram.getOrNull(i + 1);

  if (top != null) {
    if (top.getOrNull(j - 1) == '@') result++;
    if (top.getOrNull(j) == '@') result++;
    if (top.getOrNull(j + 1) == '@') result++;
  }
  if (bot != null) {
    if (bot.getOrNull(j - 1) == '@') result++;
    if (bot.getOrNull(j) == '@') result++;
    if (bot.getOrNull(j + 1) == '@') result++;
  }
  if (current.getOrNull(j - 1) == '@') result++;
  if (current.getOrNull(j + 1) == '@') result++;
  return result;
}

